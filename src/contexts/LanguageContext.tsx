export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    // Load saved language from localStorage or default to English
    return (localStorage.getItem('ticket-ballot-language') as Language) || 'en';
  });

  const setLanguage = (newLanguage: Language) => {
    setLanguageState(newLanguage);
    localStorage.setItem('ticket-ballot-language', newLanguage);
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    const currentTranslations = translations[language] as Record<string, string>;
    const fallbackTranslations = translations['en'] as Record<string, string>;
    let text = currentTranslations[key] || fallbackTranslations[key] || key;
    
    // Replace parameters like {name}, {count}, etc.
    if (params) {
      Object.keys(params).forEach(param => {
        text = text.replace(new RegExp(`{${param}}`, 'g'), String(params[param]));
      });
    }
    
    return text;
  };

  useEffect(() => {
    // Update document language attribute
    document.documentElement.lang = language;
  }, [language]);

  const value: LanguageContextType = {
    language,
    setLanguage,
    t
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}